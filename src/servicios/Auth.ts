import { CredencialesTipo } from "../tipos/CredencialesTipo";
const apiUrl = import.meta.env.VITE_API_URL || "http://localhost:10000"; // ‚úÖ fallback para desarrollo

interface Token {
  token: string;
}

// üîÑ Funci√≥n de Registro Mejorada
export async function registrarse(
  credenciales: CredencialesTipo
): Promise<Token> {
  const response = await fetch(`${apiUrl}/api/signup`, {
    method: "POST",
    body: JSON.stringify(credenciales),
    headers: {
      "Content-Type": "application/json",
      Accept: "application/json",
    },
    credentials: "include",
    mode: "cors",
  });

  if (!response.ok) {
    const errorData = await response
      .json()
      .catch(() => ({ error: "Error desconocido" }));
    throw new Error(
      errorData.error || `Registro fallido con c√≥digo: ${response.status}`
    );
  }

  const { token } = await response.json(); // üç™ Guardar en LocalStorage

  localStorage.setItem("token", token);

  return { token };
}

// üîë Funci√≥n de Login Definitiva (UX Mejorada)
export async function acceder(credenciales: CredencialesTipo): Promise<Token> {
  const response = await fetch(`${apiUrl}/api/login`, {
    method: "POST",
    body: JSON.stringify(credenciales),
    headers: {
      "Content-Type": "application/json",
      Accept: "application/json", // üëà A√±adir esta l√≠nea
    },
    credentials: "include",
    mode: "cors",
  });

  if (!response.ok) {
    // Intentamos leer el cuerpo del error como JSON, si falla, usamos un objeto vac√≠o
    const errorData = await response.json().catch(() => ({}));
    let errorMessage = "Ocurri√≥ un error desconocido. Int√©ntalo m√°s tarde.";

    if (response.status === 401) {
      // Caso 401: Credenciales inv√°lidas (Clave incorrecta)
      errorMessage =
        errorData.message ||
        "Usuario o clave incorrectos. Verifica tus credenciales.";
      throw new Error(errorMessage);
    }
    if (response.status === 400) {
      // Caso 400: Error de validaci√≥n (Clave demasiado corta, etc.)
      // Si el backend env√≠a un array de errores de validaci√≥n (como de express-validator)
      if (
        errorData.errors &&
        Array.isArray(errorData.errors) &&
        errorData.errors.length > 0
      ) {
        // Tomamos el primer mensaje espec√≠fico de validaci√≥n para mostrar
        errorMessage =
          errorData.errors[0].msg || "Error de validaci√≥n de datos.";
        throw new Error(errorMessage);
      } // Fallback para 400 si no hay array de errores

      errorMessage = errorData.message || `Datos inv√°lidos (C√≥digo 400).`;
      throw new Error(errorMessage);
    } // Error gen√©rico para otros fallos (500, etc.)

    throw new Error(errorMessage);
  }

  const data = await response.json();
  const token = data.token; // Almacena solo en localStorage para simplicidad

  localStorage.setItem("token", token);

  return { token };
}

// üóëÔ∏è Funci√≥n de Eliminaci√≥n de Usuario
export async function eliminarUsuario(
  token: string,
  usuarioId: number
): Promise<void> {
  try {
    const response = await fetch(`${apiUrl}/api/usuarios/${usuarioId}`, {
      method: "DELETE",
      headers: {
        Authorization: `Bearer ${token}`,
      },
      credentials: "include",
    });

    if (!response.ok) {
      let errorMessage = "Error al eliminar el usuario";
      if (response.status === 401) {
        errorMessage = "No autorizado. Inicia sesi√≥n nuevamente.";
      } else if (response.status === 404) {
        errorMessage = "Usuario no encontrado.";
      }
      throw new Error(errorMessage);
    }

    localStorage.clear();
    document.cookie = "token=; Max-Age=0; Path=/";
  } catch (error) {
    throw new Error(
      `Error cr√≠tico: ${error instanceof Error ? error.message : "Desconocido"}`
    );
  }
}

// üö™ Cierre de Sesi√≥n Robustecido
export function cerrarSesion(): void {
  localStorage.removeItem("token");
  document.cookie = "token=; Max-Age=0; Path=/";
  window.location.href = "/inicio";
}
